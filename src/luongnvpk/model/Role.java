package luongnvpk.model;

public class Role extends BaseModel{
	String name;
	String typeRole;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTypeRole() {
		return typeRole;
	}
	public void setTypeRole(String typeRole) {
		this.typeRole = typeRole;
	}
	
	
}
